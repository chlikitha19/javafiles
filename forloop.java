public class forloop {
    public static void main(String[] args) {
        //for loop = executes a code for certain number of times
        //so we have 3 statements here in for loop which are seperated by semicolon
        // first statement is the initialisation basically we are creating a number, second statement is the condition upto where the loop should continue
        // third statement is the increament or decrement

        for (int i =0;i<20;i+=2){
        
                System.out.println(i);
            }

        }
    }
    
