public class nestedloop {
    public static void main(String[] args) {
        // nested loop is the loop inside an another loop
        // used mostly in construction of a matrix or dsa concepts

        for(int i = 1; i<=9; i++){
            for(int j = 1;j<=9; j++){
              System.out.print(j+" ");
        }
        System.out.println();
    }
    }
    
}
